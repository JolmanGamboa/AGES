    public void actualizarTabla() {
        ProductoController productoController = new ProductoController();
        Object[][] datos = productoController.getDatosProductos();
        String[] columnas = productoController.getNombresColumnas();
        
        model = new DefaultTableModel(datos, columnas) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };
        
        if (tablaProductos == null) {
            tablaProductos = new JTable(model);
            tablaProductos.setFillsViewportHeight(true);
            
            // Configurar el scroll pane
            scrollPane = new JScrollPane(tablaProductos);
            scrollPane.setBounds(150, 50, 400, 200);
            
            // Reemplazar el JTextArea con la tabla
            jPanel2.remove(jScrollPane2);
            jPanel2.add(scrollPane);
        } else {
            tablaProductos.setModel(model);
            tablaProductos.revalidate();
            tablaProductos.repaint();
        }
    }


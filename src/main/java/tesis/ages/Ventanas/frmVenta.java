    private void jBtoConfirmarActionPerformed(java.awt.event.ActionEvent evt) {
        String idStr = JOptionPane.showInputDialog(this, "Ingrese el ID del producto a eliminar:");
        if (idStr == null || idStr.trim().isEmpty()) {
            return;
        }
        
        try {
            int id = Integer.parseInt(idStr);
            ventaController.eliminarProducto(id);
            actualizarTabla();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El ID debe ser un número");
        }
    }

    private void jBtoConfirmar1ActionPerformed(java.awt.event.ActionEvent evt) {
        String numeroFactura = jTextFactura.getText().trim();
        String referencia = jTextFactura1.getText().trim();
        String cantidadStr = jTextFactura2.getText().trim();
        
        
        // Validar campos vacíos
        if (numeroFactura.isEmpty() || referencia.isEmpty() || cantidadStr.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios");
            return;
        }
        
        // Validar número de factura
        if (!ventaController.validarNumeroFactura(numeroFactura)) {
            JOptionPane.showMessageDialog(null, "El número de factura debe ser un número positivo");
            return;
        }
        
        try {
            int cantidad = Integer.parseInt(cantidadStr);
            if (cantidad <= 0) {
                JOptionPane.showMessageDialog(null, "La cantidad debe ser un número positivo");
                return;
            }
            
            // Obtener la fecha seleccionada
            java.util.Date fechaSeleccionada = jDateFecha.getDate();
            if (fechaSeleccionada == null) {
                JOptionPane.showMessageDialog(null, "Por favor seleccione una fecha");
                return;
            }
            

            // Formatear fecha
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
            //String fecha = sdf.format(jDateFecha.getDate());
            String fecha = sdf.format(fechaSeleccionada);
            
            // Agregar producto a la tabla
            ventaController.agregarProductoATabla(numeroFactura, fecha, referencia, cantidad);
            actualizarTabla();
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "La cantidad debe ser un número");
        }
    }


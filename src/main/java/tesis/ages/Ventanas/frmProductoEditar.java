    private void jBtoConfirmarActionPerformed(java.awt.event.ActionEvent evt) {                                             
        String idStr = ID.getText().trim();
        
        // Validar que el campo ID no esté vacío
        if (idStr.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese el ID del producto a editar", 
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Validar que sea un número
        try {
            int idProducto = Integer.parseInt(idStr);
            
            // Obtener los valores de los campos
            String referencia = Codigo.getText();
            String nombre = jTextNombre.getText();
            String cantidadStr = jTextCantidad.getText();
            String valorUStr = jTextValorUni.getText();
            String alertaStr = jTextAlerta.getText();
            
            // Validar que los campos no estén vacíos
            if (referencia.isEmpty() || nombre.isEmpty() || cantidadStr.isEmpty() || valorUStr.isEmpty() || alertaStr.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Todos los campos son obligatorios", 
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Convertir y validar valores numéricos
            int cantidad = Integer.parseInt(cantidadStr);
            int valorUnitario = Integer.parseInt(valorUStr);
            int alerta = Integer.parseInt(alertaStr);
            
            // Validar que la cantidad no sea negativa
            if (cantidad < 0) {
                JOptionPane.showMessageDialog(null, "La cantidad no puede ser negativa", 
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            // Crear instancia del controlador y actualizar el producto
            ProductoController productoController = new ProductoController();
            if (productoController.actualizarProducto(idProducto, referencia, nombre, cantidad, alerta, valorUnitario)) {
                // Actualizar la tabla en el formulario principal si existe
                if (frmProducto.fp != null) {
                    frmProducto.fp.actualizarTabla();
                }
                dispose();
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Los campos de cantidad, valor unitario y alerta deben ser números", 
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }                                            


    private void jBtoBuscarActionPerformed(java.awt.event.ActionEvent evt) {                                             
        String idStr = ID.getText().trim();
        
        // Validar que el campo no esté vacío
        if (idStr.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor ingrese el ID del producto a editar", 
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        // Validar que sea un número
        try {
            int idProducto = Integer.parseInt(idStr);
            
            // Crear instancia del controlador y buscar el producto
            ProductoController productoController = new ProductoController();
            Object[] producto = productoController.buscarProducto(idProducto);
            
            if (producto != null) {
                // Rellenar los campos con la información del producto
                Codigo.setText((String) producto[0]); // Referencia
                jTextNombre.setText((String) producto[1]);
                jTextCantidad.setText(String.valueOf(producto[2]));
                jTextAlerta.setText(String.valueOf(producto[3]));
                jTextValorUni.setText(String.valueOf(producto[4]));
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró el producto con el ID especificado", 
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El ID debe ser un número entero", 
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }                                            

